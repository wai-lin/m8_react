import { isSameDay } from "date-fns"

type WordOfTheDay = {
	word: string
	date: null | string
}

const storageKey = "WOD"

export const wordOfTheDay = () => {
	const storedWord = JSON.parse(
		localStorage.getItem(storageKey) ?? '{"word":"","date":null}',
	) as WordOfTheDay

	const now = new Date()
	const randomWord =
		words[Math.floor(Math.random() * words.length)].toUpperCase()

	if (storedWord.date === null || storedWord.word === "") {
		storedWord.word = randomWord
		storedWord.date = now.toString()
		localStorage.setItem(storageKey, JSON.stringify(storedWord))
		return storedWord.word
	}

	if (isSameDay(new Date(storedWord.date), now) && storedWord.word !== "") {
		return storedWord.word
	} else {
		storedWord.word = randomWord
		storedWord.date = now.toString()
		localStorage.setItem(storageKey, JSON.stringify(storedWord))
		return storedWord.word
	}
}

